def main():
    Base.metadata.create_all(bind=engine)
    db = SessionLocal()
    try:
        teacher = db.query(User).filter(User.username == "teacher").first()
        uid = teacher.id if teacher else None
        added = 0
        for c in PRESET_CASES:
            exists = db.query(CaseItem).filter(CaseItem.title == c["title"]).first()
            if exists:
                continue
            db.add(CaseItem(
                title=c["title"],
                description=c["description"],
                tags=c["tags"],
                created_by=uid,
            ))
            added += 1
        db.commit()
        if added:
            print(f"已写入 {added} 条预置案例。")
        else:
            print("已存在预置案例，跳过。")
    finally:
        db.close()


if __name__ == "__main__":
    main()
